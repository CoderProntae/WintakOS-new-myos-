static void font_begin_access(void)
{
    outb(0x3C4, 0x00); outb(0x3C5, 0x01);
    outb(0x3C4, 0x02); outb(0x3C5, 0x04);
    outb(0x3C4, 0x04); outb(0x3C5, 0x07);
    outb(0x3C4, 0x00); outb(0x3C5, 0x03);

    outb(0x3CE, 0x04); outb(0x3CF, 0x02);
    outb(0x3CE, 0x05); outb(0x3CF, 0x00);
    outb(0x3CE, 0x06); outb(0x3CF, 0x00);
}

static void font_end_access(void)
{
    outb(0x3C4, 0x00); outb(0x3C5, 0x01);
    outb(0x3C4, 0x02); outb(0x3C5, 0x03);
    outb(0x3C4, 0x04); outb(0x3C5, 0x03);
    outb(0x3C4, 0x00); outb(0x3C5, 0x03);

    outb(0x3CE, 0x04); outb(0x3CF, 0x00);
    outb(0x3CE, 0x05); outb(0x3CF, 0x10);
    outb(0x3CE, 0x06); outb(0x3CF, 0x0E);
}

static void write_glyph(volatile uint8_t* font_mem, uint8_t index, const uint8_t bitmap[16])
{
    uint32_t off = (uint32_t)index * 32;
    for (int i = 0; i < 16; i++) {
        font_mem[off + i] = bitmap[i];
    }
}

void vga_font_install_turkish(void)
{
    volatile uint8_t* font_mem = (volatile uint8_t*)0xA0000;

    font_begin_access();

    write_glyph(font_mem, CHAR_TR_DOTLESS_I,    glyph_dotless_i);
    write_glyph(font_mem, CHAR_TR_DOTTED_CAP_I, glyph_dotted_I);
    write_glyph(font_mem, CHAR_TR_S_CEDILLA,    glyph_s_cedilla);
    write_glyph(font_mem, CHAR_TR_S_CEDILLA_UP, glyph_S_cedilla);
    write_glyph(font_mem, CHAR_TR_G_BREVE,      glyph_g_breve);
    write_glyph(font_mem, CHAR_TR_G_BREVE_UP,   glyph_G_breve);
    write_glyph(font_mem, CHAR_TR_U_UMLAUT,     glyph_u_umlaut);
    write_glyph(font_mem, CHAR_TR_U_UMLAUT_UP,  glyph_U_umlaut);
    write_glyph(font_mem, CHAR_TR_O_UMLAUT,     glyph_o_umlaut);
    write_glyph(font_mem, CHAR_TR_O_UMLAUT_UP,  glyph_O_umlaut);
    write_glyph(font_mem, CHAR_TR_C_CEDILLA,     glyph_c_cedilla);
    write_glyph(font_mem, CHAR_TR_C_CEDILLA_UP,  glyph_C_cedilla);

    font_end_access();
}
