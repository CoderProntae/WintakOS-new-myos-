/*============================================================================
 * Turkce Q Klavye Haritasi
 *
 * CP437 Turkce karakterler:
 *   ü = 0x81    Ü = 0x9A
 *   ö = 0x94    Ö = 0x99
 *   ç = 0x87    Ç = 0x80
 *
 * Ozel font glifleri:
 *   ı = 0x01    İ = 0x02
 *   ş = 0x03    Ş = 0x04
 *   ğ = 0x05    Ğ = 0x06
 *
 * Turkce Q fiziksel pozisyonlar:
 *   0x17 (US i)  → ı / I (shift)
 *   0x28 (US ')  → i / İ (shift)
 *   0x27 (US ;)  → ş / Ş (shift)
 *   0x1A (US [)  → ğ / Ğ (shift)
 *   0x1B (US ])  → ü / Ü (shift)
 *   0x33 (US ,)  → ö / Ö (shift)
 *   0x34 (US .)  → ç / Ç (shift)
 *   0x35 (US /)  → . / : (shift)
 *   0x2B (US \)  → , / ; (shift)
 *   0x29 (US `)  → " / é (shift, simdilik ")
 *   0x0C (US -)  → * / ? (shift)
 *   0x0D (US =)  → - / _ (shift)
 *==========================================================================*/

